# 자료형 (Data Type) : 자료의 유형, 자료의 형식
# 문자열(String)
# 숫자(Number) : integer, float
# 불린(Boolean) : True, False

print(type(3))
print(type("Hello"))
print(type(True))

a = "10"
print(a)
print(type(a))

b = int(a)
print(b)
print(type(b))

c = False
print(c)
print(type(c))

d = "ABCDEFG"
print(d[-5])
print(d[0])
print(d[1])
print(d[1:3])
print(d[:5])
print(d[5:])
print(len(d))
